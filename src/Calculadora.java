package CalculadoraArquivo;

public class Calculadora {
	
	public int soma (int x, int y){
		return x + y;
	}
	
	public int subtração (int x, int y){
		return x - y;
	}
	
	public int multiplicação (int x, int y){
		return x * y;
	}
	
	public int divisão (int x, int y){
		return x / y;
	}

}
